    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        PrintWriter out = response.getWriter();
        HttpSession ses = request.getSession();
        User user = (User) ses.getAttribute("acc");
        if (user != null) {
            User usr = getFullUs(user.getUserID());
            request.setAttribute("us", usr);
            request.getRequestDispatcher("./Views/US/changeInfor.jsp").forward(request, response);
        }else{
            response.sendRedirect("login");
        }
    }

    public User getFullUs(String usid) {
        DAO dao = new DAO();
        for (User u : dao.getUs()) {
            if (u.getUserID().equals(usid)) {
                return u;
            }
        }
        return null;
    }

